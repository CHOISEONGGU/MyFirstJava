public class Solution3 {
    public static void main(String[] args) {
        // boolean (참/거짓)
        // Python과 다른 점 : 소문자로 표현한다
        // Python은 True, False / Java는 true, false
        System.out.println(true);
        System.out.println(false);
        System.out.println(3 > 5);
        System.out.println(3 < 5);
        System.out.println(3 == 5);
        System.out.println(3 != 5);
        System.out.println(3 >= 5);
        System.out.println(3 <= 5);
        // 파이썬에서는? and or not
        System.out.println(3 == 3 & 4 == 4); // and 연산, 그리고 연산.
        System.out.println(3 == 3 && 4 == 4); // 빠른 and 연산
        System.out.println(3 == 3 | 4 == 4); // or, 또는
        System.out.println(3 == 3 || 4 == 4); // 빠른 or
    }
}
